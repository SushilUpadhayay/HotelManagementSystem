        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Mydb", "root", "5130");
            Statement st = (Statement) con.createStatement();
            String tGmail = EmployeeDetails.Gmail;
            String sql = "SELECT * FROM EmployeeAccount WHERE Gmail = '" + tGmail + "'";// here Gmail of each user is unique
            ResultSet rs = st.executeQuery(sql);
            if(rs.next())
            {
                jTextField1.setText(rs.getString("Name"));
                jTextField2.setText(rs.getString("Gmail"));
                jTextField3.setText(rs.getString("Age"));
                jTextField4.setText(rs.getString("Shift"));
                jTextField5.setText(rs.getString("Gender"));
                jTextField6.setText(rs.getString("Role"));
                jTextField7.setText(rs.getString("Salary"));
                jTextField8.setText(rs.getString("Department"));
            }
            else
            {
                // Nothing
            }
            con.close();
        }
