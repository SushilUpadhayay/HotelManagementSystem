    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Submit Button'
        String Name = jTextField1.getText();
        String Gmail  = jTextField2.getText();
        String Age = jTextField3.getText();
        String Gender;
        if (jRadioButton1.isSelected() == true)
        {
            Gender = "Male";
        }
        else if(jRadioButton2.isSelected() == true)
        {
            Gender = "Female";
        }
        else
        {
            Gender = "Other";
        }
        String Password = jTextField6.getText();
        String Password2 = jTextField5.getText(); // the default string return by the text fields are empty string not null string
        if (!Name.equals("")&& !Gmail.equals("")&& !Age.equals("")&& !Password.equals("") && !Password2.equals("")) // user needs to enter all of the textfields 
        {
            if(Password.equals(Password2))
            {
               try
               {  
                 Class.forName("com.mysql.cj.jdbc.Driver");
                 Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Mydb", "root", "5130");
                 Statement st = (Statement) con.createStatement();
                 String sql = "SELECT * FROM Customer WHERE Gmail = '" + Gmail + "'";// here Gmail of each user is unique
                 ResultSet rs = st.executeQuery(sql);
                 if(rs.next()) // if the gmail is already used then we can't accept that gmail
                 {
                   JOptionPane.showMessageDialog(this, "Your Gmail has been already used");
                 }
                 else
                 {
                   String sql2 = "INSERT INTO Customer (Name, Gmail, Age, Gender, Password) VALUES ('"
                                + Name + "', '" + Gmail + "', '" + Age + "', '" + Gender + "', '" + Password + "')";
                   st.executeUpdate(sql2);
                   JOptionPane.showMessageDialog(this, "Your Customer Account has been sucessfully created");
                 }
                 con.close();
               }
                catch(Exception e)
                {
                   System.out.println(e.getMessage());
                } 
            }
            else
            {
              JOptionPane.showMessageDialog(this, "Passwords do not match. Re-enter password.");          
            }     
        } 
        else 
        {
            JOptionPane.showMessageDialog(this, "All the fields are mandatory");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

